package com.c4e.oauth;

public class AuthInfo {
	private String user;
	private String host;
	private String base64_cert;
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getBase64_cert() {
		return base64_cert;
	}
	public void setBase64_cert(String base64_cert) {
		this.base64_cert = base64_cert;
	}
	
	
}
